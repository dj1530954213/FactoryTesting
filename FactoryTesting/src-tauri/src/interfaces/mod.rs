//! # 接口适配层 (Interfaces Layer)
//!
//! ## 业务说明
//! 接口适配层负责处理外部系统与业务核心的交互，将外部请求转换为业务操作
//! 主要功能包括API端点定义、请求响应处理、数据格式转换等
//!
//! ## 架构职责
//! - **Tauri命令适配**: 将前端调用转换为后端业务操作
//! - **数据转换**: 处理前后端数据格式的转换和验证
//! - **错误处理**: 将业务层错误转换为用户友好的响应
//! - **权限验证**: 处理接口级别的权限控制（如需要）
//!
//! ## 设计模式
//! - **适配器模式**: 适配不同的外部接口格式
//! - **门面模式**: 为复杂的业务流程提供简单的接口
//! - **命令模式**: 将请求封装为命令对象
//!
//! ## 调用链路
//! 前端 → Tauri IPC → 接口适配层 → 应用层 → 领域层 → 基础设施层
//!
//! ## Rust知识点
//! - **异步函数**: 使用async/await处理异步操作
//! - **宏系统**: 利用Tauri的命令生成宏
//! - **错误传播**: 使用?操作符进行错误传播

